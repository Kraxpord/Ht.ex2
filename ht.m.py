day = int(input("Введіть день - "))
if day == 1:
    print('Понеділок')
elif day == 2:
    print('Вівторок')
elif day == 3:
    print('Середа')
elif day == 4:
    print('Четверг')
elif day == 5:
    print('Пятниця')
elif day == 6:
    print('Субота')
elif day == 7:
    print('Неділя')
else:
    print('Помилка!')